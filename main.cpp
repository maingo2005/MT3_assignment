Matrix4x4 MakeTranslateMatrix(const Vector3& translate) {
	Matrix4x4 T;

	T.m[0][0] = 1;
	T.m[0][1] = 0;
	T.m[0][2] = 0;
	T.m[0][3] = 0;

	T.m[1][0] = 0;
	T.m[1][1] = 1;
	T.m[1][2] = 0;
	T.m[1][3] = 0;

	T.m[2][0] = 0;
	T.m[2][1] = 0;
	T.m[2][2] = 1;
	T.m[2][3] = 0;

	T.m[3][0] = translate.x;
	T.m[3][1] = translate.y;
	T.m[3][2] = translate.z;
	T.m[3][3] = 1;

	return T;
}

Matrix4x4 Multiply(const Matrix4x4& m1, const Matrix4x4& m2) {
	Matrix4x4 multiply;

	for (int m = 0; m < 4; m++) {
		for (int n = 0; n < 4; n++) {
			multiply.m[m][n] = 0;
			for (int mn = 0; mn < 4; mn++) {
				multiply.m[m][n] += m1.m[m][mn] * m2.m[mn][n];
			}
		}
	}

	return multiply;
}

