//クロス積
Vector3 Cross(const Vector3& v1, const Vector3& v2) {
	Vector3 v{ 0.0f,0.0f,0.0f };

	v.x = (v1.z * v2.x - v1.x * v2.z, v1.x * v2.y - v1.y * v2.x, v1.y * v2.z - v1.z * v2.y);
	v.y = (v1.x * v2.y - v1.y * v2.x, v1.y * v2.z - v1.z * v2.y, v1.z * v2.x - v1.x * v2.z);
	v.z = (v1.y * v2.z - v1.z * v2.y, v1.z * v2.x - v1.x * v2.z, v1.x * v2.y - v1.y * v2.x);

	return v;

}

//4x4拡大縮小行列
Matrix4x4 MakeScaleMatrix(const Vector3& scale) {
	Matrix4x4 S;

	S.m[0][0] = scale.x;
	S.m[0][1] = 0;
	S.m[0][2] = 0;
	S.m[0][3] = 0;

	S.m[1][0] = 0;
	S.m[1][1] = scale.y;
	S.m[1][2] = 0;
	S.m[1][3] = 0;

	S.m[2][0] = 0;
	S.m[2][1] = 0;
	S.m[2][2] = scale.z;
	S.m[2][3] = 0;

	S.m[3][0] = 0;
	S.m[3][1] = 0;
	S.m[3][2] = 0;
	S.m[3][3] = 1;

	return S;
}

//X軸回転行列
Matrix4x4 MakeRotateXMatrix(float radian) {
	Matrix4x4 X;

	X.m[0][0] = 1;
	X.m[0][1] = 0;
	X.m[0][2] = 0;
	X.m[0][3] = 0;

	X.m[1][0] = 0;
	X.m[1][1] = std::cos(radian);
	X.m[1][2] = std::sin(radian);
	X.m[1][3] = 0;

	X.m[2][0] = 0;
	X.m[2][1] = std::sin(-radian);
	X.m[2][2] = std::cos(radian);
	X.m[2][3] = 0;

	X.m[3][0] = 0;
	X.m[3][1] = 0;
	X.m[3][2] = 0;
	X.m[3][3] = 1;

	return X;
}

